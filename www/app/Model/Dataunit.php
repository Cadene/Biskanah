    }

    public function findAllByDatabuilding($databuilding_id)
    {
        $tmp = $this->find('all',array(
            'recursive' => -1,
            'conditions' => array(
                'Dataunit.databuilding_id' => $databuilding_id
            )
        ));
        $init = current($tmp[0])['id'];
        for ($i = $init; $i < count($tmp)+$init; $i++) {
            $rslt[$i] = $tmp[$i-$init];
        }
        return $rslt;
    }



